def hello(first_name,last_name,age): #inside the parenthesis is the parameter
    print("hello "+first_name+""+last_name)
    print("you are "+str(age)+" years old")
    print("have a nice day")



#hello() #calls the function
hello("joey " , "collado" ,19) #calls the function using the parameters set which is the name as we give a string value

