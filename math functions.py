import math

pi = 3.14

x = 1
y = 2
z = 3

print(round(pi)) #round up number
print(math.ceil(pi)) #round up to the nearest integer
print(math.floor(pi)) #round down
print(abs(pi)) #take the absolute value of the integer
print(pow(pi,2)) #raise integer to the power, here is pi to the power of 2
print(math.sqrt(pi)) #take the square root of the integer
print(max(x,y,z)) #take the highest value of varying number
print(min(x,y,z)) #take the lowest value of varying number