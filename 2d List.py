# 2d lists = a list of lists

drinks = ["coffee", "soda", "juice"]
dinner = ["pizza", "burger", "taco"]
dessert = ["ice cream", "cake", "flan"]

food = [drinks,dinner,dessert]

print(food) #prints all elements
print(food[0]) #prints a specific list using index
print(food[0][0]) #prints specific element on a specific list